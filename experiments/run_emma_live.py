"""LIVE run: the real Emma (diffusiongemma-26B on DGX) teaches Sèvo French plurals.

Unlike the offline experiments, here the teaching items are *generated by a live
local model* through the LiteLLM stack. The model only proposes words; the
curriculum vets them and computes the correct plural, and the oracle grades on
curated held-out / transfer banks. So a live, non-deterministic teacher still
produces an auditable, leakage-free measurement.

Backend: DGX vLLM ``:8010`` served-name ``code-nvfp4`` (diffusiongemma-26B-A4B
NVFP4), reached directly (gateway bypass) — no API key, local model, zero cost.
``enable_thinking:false`` per talki R-501.

This is an explicitly operator-authorised live run (the EmmaLiteLLM adapter stays
inert by default; here we inject the transport on purpose).

Run:  PYTHONPATH=src:experiments python3 experiments/run_emma_live.py
Override backend: SEVO_LLM_URL=http://xeon:4000 SEVO_LLM_MODEL=code ...
"""
from __future__ import annotations

import json
import os
import sys
import time

from sevo.brain import Brain
from sevo.curriculum.fr_cp_ce1 import build_bank_fr, transfer_bank_fr
from sevo.eval import compute_delta
from sevo.teacher.emma_litellm import GEMMA_EXTRA_BODY, EmmaLiteLLM, LiteLLMTransport

URL = os.environ.get("SEVO_LLM_URL", "http://100.118.62.11:8010")
MODEL = os.environ.get("SEVO_LLM_MODEL", "code-nvfp4")
NODES = ["fr.CE1.pluriel_reguliers", "fr.CE1.pluriel_en_al", "fr.CE2.pluriel_invariables"]
SESSIONS_PER_NODE = 5
WORDS_PER_SESSION = 8
SEED = 13


def _eval_banks():
    heldout, transfer = [], transfer_bank_fr()
    for nid in NODES:
        heldout += build_bank_fr(nid, _seeded(nid)).heldout
    return heldout, transfer


def _seeded(tag):
    from sevo.rng import Rng
    return Rng(SEED).fork(tag)


def run() -> dict:
    transport = LiteLLMTransport(URL, master_key="", model=MODEL, extra_body=GEMMA_EXTRA_BODY)
    emma = EmmaLiteLLM(transport=transport, model=MODEL)
    brain = Brain(seed=SEED)

    heldout, transfer = _eval_banks()
    pre = {"heldout": brain.evaluate(heldout, "pre.heldout"),
           "transfer": brain.evaluate(transfer, "pre.transfer")}

    t0 = time.time()
    teaching_trace, llm_calls = [], 0
    for nid in NODES:
        proposed = []
        for _ in range(SESSIONS_PER_NODE):
            tasks = emma.generate_french_tasks(nid, WORDS_PER_SESSION)
            llm_calls += 1
            if not tasks:
                continue
            proposed += [t.word for t in tasks]
            brain.learn_session("CE1", "français", tasks)
        teaching_trace.append({"node": nid, "emma_proposed_words": proposed,
                               "final_mastery": round(brain.semantic.mastery(nid), 3)})

    brain.consolidate("sleep", advance_days=1)
    brain.consolidate("error_replay", advance_days=0)

    t1 = {"heldout": brain.evaluate(heldout, "t1.heldout"),
          "transfer": brain.evaluate(transfer, "t1.transfer")}
    brain.advance_days(7)
    t2 = {"heldout": brain.evaluate(heldout, "t2.delayed")}

    delta = compute_delta(
        heldout_before=pre["heldout"]["accuracy"], heldout_after=t1["heldout"]["accuracy"],
        transfer_before=pre["transfer"]["accuracy"], transfer_after=t1["transfer"]["accuracy"],
        reasoning_before=pre["transfer"]["accuracy"], reasoning_after=t1["transfer"]["accuracy"],
        t1_after=t1["heldout"]["accuracy"], t2_after=t2["heldout"]["accuracy"],
        calibration_before=pre["heldout"]["calibration_error"],
        calibration_after=t1["heldout"]["calibration_error"],
        trials_with_prereq=1, trials_without_prereq=1,  # efficiency control omitted in live run
    )
    return {
        "mode": "LIVE", "backend": {"url": URL, "model": MODEL, "enable_thinking": False},
        "llm_calls": llm_calls, "wall_seconds": round(time.time() - t0, 1),
        "pretest": pre, "teaching_trace": teaching_trace,
        "posttest_t1": t1, "posttest_t2": t2, "intelligence_delta": delta,
    }


if __name__ == "__main__":
    result = run()
    json.dump(result, sys.stdout, indent=2, ensure_ascii=False)
    sys.stdout.write("\n")
